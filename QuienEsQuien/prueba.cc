/* PROYECTO:

PRIMER PASO: Iniciar partida
-Menú con Join/Host, ambos te piden dirección y puerto, uno abre el servidor y el otro trata de conectarse
-Pedirte un nombre de usuario
-El host tiene que esperar a que se una el cliente
-Al iniciar se asigna una de las caras de forma aleatoria a cada jugador
-Cuando se una empieza el turno siempre el host

//++++++++++++++++++++++++++++++++++++++++++++

SEGUNDO PASO: Bucle de juego
-2 opciones: Preguntar o resolver

    Preguntar:
-Jugador A escribe pregunta sobre la cara del jugador B
-Enviar pregunta
-Jugador B responde SI/NO (en boton o por escrito)
-Jugador A baja las caras que crea correspondiente (clic derecho)
-Empieza el turno del jugador B

    Resolver:
-Jugador A selecciona la cara que crea correspondiente (clic izquierdo) y pulsa el boton Resolver
    -Acierta: Gana la partida
    -Se equivoca: Pierde la partida

//++++++++++++++++++++++++++++++++++++++++++++

TERCER PASO: Fin de partida
-Menú con dos botones NuevaPartida/Salir
    -Nueva partida: Compobar las conexiones y reasignar nuevas caras a los jugadores -> Paso 2
    -Salir: Desconectar

*/